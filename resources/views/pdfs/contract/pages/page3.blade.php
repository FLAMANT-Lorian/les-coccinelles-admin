<h1>
    Règlement en vigueur<br>
    <strong>à partir du 1er janvier 2024</strong>
</h1>

<section>
    <h2 class="title">Préambule</h2>

    <p>
        L'ASBL « LES COCCINELLES » a été créée en 1996 (Moniteur belge n° 13720/96)
        et est propriétaire de la « Salle des COCCINELLES ».
    </p>

    <p>
        Elle a été créée dans le but de construire et gérer la salle de village et permettre ainsi
        aux citoyens et groupements des villages de Morhet, Morhet Gare et Remience,
        d’organiser des activités socio culturelles, familiales et/ou sportives.
    </p>

    <p>
        Le conseil d’administration de l’A.S.B.L. « LES COCCINELLES » est constitué de membres effectifs
        et il est seul autorisé à engager l’A.S.B.L. concernant tout ce qui touche directement ou indirectement
        à la gestion et à l’entretien de la salle.
    </p>

    <p>
        Les membres s’engagent à poursuivre les buts de l’A.S.B.L. et à se soumettre aux statuts et
        règlements prévus par son comité de gestion.
    </p>
</section>

