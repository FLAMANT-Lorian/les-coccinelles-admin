<section>
    <h2 class="title">Règlement général</h2>

    <p>
        <strong>1. Assurances.</strong><br>
        Les utilisateurs s’engagent à respecter les lois en vigueur en ce qui concerne les assurances nécessaires
        à l’usage prévu des installations. L’assurance de l’ASBL « LES COCCINELLES » ne couvre en responsabilité
        civile que les activités qu’elle organise en son nom.
    </p>

    <p>
        <strong>2. Accises, SABAM ou autres réglementations.</strong><br>
        Tout utilisateur est tenu de payer les droits d’accises pour les boissons, droits d’auteur ou autres droits applicables.
        L’ASBL ne sera en rien responsable des amendes éventuelles.
    </p>

    <p>
        <strong>3. Occupation de la salle.</strong><br>
        Toute occupation autre que préparation ou nettoyage doit être autorisée par le responsable du planning.
        Elle sera facturée 50€ la première journée et 100€ par journée supplémentaire.
    </p>

    <p>
        <strong>4. Stocks de boissons.</strong><br>
        Les stocks sont propriété de l’ASBL et ne peuvent être utilisés sans accord.
        Un inventaire doit être réalisé avant et après usage.
    </p>

    <p>
        <strong>5. Nettoyage.</strong><br>
        L’utilisateur doit débarrasser les lieux de tous déchets et objets.
    </p>

    <p>
        <strong>6. Nettoyage optionnel.</strong><br>
        Le nettoyage peut être effectué par le comité au tarif de 10€/heure avec un minimum de 100€.
    </p>

    <p>
        <strong>7. Caution et dégâts.</strong><br>
        En cas de dégât supérieur à 50€, la caution est bloquée jusqu’à réparation.
        Le coût des réparations est à charge de l’utilisateur avec majoration de 10%.
    </p>

    <p>
        <strong>8. Responsabilité.</strong><br>
        Le signataire reste responsable des dégradations, y compris aux installations annexes.
    </p>

    <p>
        <strong>9. Réservation.</strong><br>
        La réservation est effective uniquement après paiement sur le compte de l’ASBL.
    </p>

    <p>
        <strong>10. Litiges.</strong><br>
        Les cas non prévus sont tranchés par le comité de l’ASBL.
    </p>

    <p>
        <strong>11. COVID-19.</strong><br>
        Les règles sanitaires en vigueur doivent être respectées.
    </p>
</section>
